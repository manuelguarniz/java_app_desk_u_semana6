    private void btnProcessClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessClick
        String teacherFirstname = txtTeacherName.getText();
        String teacherLastname = txtTeacherLastname.getText();
        Integer teacherAge = Integer.parseInt(txtTeacherAge.getText());
        Integer teacherFee = Integer.parseInt(txtTeacherFee.getText());
        Double teacherHoursWorked = Double.parseDouble(txtTeacherHoursWorked.getText());
        
        Teacher teacher = new Teacher(teacherFirstname, teacherLastname, teacherAge,
                                        teacherFee, teacherHoursWorked);
        
        String studentFirstname = txtStudentFirstname.getText();
        String studentLastname = txtStudentLastname.getText();
        Integer studentAge = Integer.parseInt(txtStudentAge.getText());
        Double studentNote1 = Double.parseDouble(txtStudentNote1.getText());
        Double studentNote2 = Double.parseDouble(txtStudentNote2.getText());
        Double studentNote3 = Double.parseDouble(txtStudentNote3.getText());
        
        Student student = new Student(studentFirstname, studentLastname, studentAge,
                studentNote1, studentNote2, studentNote3);
        
        txtTeacherResume.setText(teacher.showInformation());
        txtStudentResume.setText(student.showInformation());
        
    }//GEN-LAST:event_btnProcessClick

    private void txtTeacherFeeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTeacherFeeFocusLost
        try {
            if (txtTeacherFee.getText() != null && !txtTeacherFee.getText().equalsIgnoreCase("")) {
                Double fee = Double.parseDouble(txtTeacherFee.getText());
                if (fee < 0) {
                    JOptionPane.showMessageDialog(null, "Tarifa debe ser mayor que 0 !");
                    txtTeacherFee.requestFocus();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tarifa es invalido !");
            txtTeacherFee.requestFocus();
        }
    }//GEN-LAST:event_txtTeacherFeeFocusLost

