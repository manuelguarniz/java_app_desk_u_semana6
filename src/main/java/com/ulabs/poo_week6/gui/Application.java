    private void txtTeacherAgeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTeacherAgeFocusLost
        try {
            if (txtTeacherAge.getText() != null && !txtTeacherAge.getText().equalsIgnoreCase("")) {
                Integer age = Integer.parseInt(txtTeacherAge.getText());
                if (age < 18) {
                    JOptionPane.showMessageDialog(null, "Es menor de edad !");
                    txtTeacherAge.requestFocus();
                } else if (age > 65) {
                    JOptionPane.showMessageDialog(null, "Ya es hora de jubilarse !");
                    txtTeacherAge.requestFocus();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Edad es invalido !");
            txtTeacherAge.requestFocus();
        }
    }//GEN-LAST:event_txtTeacherAgeFocusLost

